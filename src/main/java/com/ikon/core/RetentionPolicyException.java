package com.ikon.core;

public class RetentionPolicyException extends Exception {
private static final long serialVersionUID = 1L;
	
	public RetentionPolicyException() {
		super();
	}

	public RetentionPolicyException(String arg0) {
		super(arg0);
	}

	public RetentionPolicyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RetentionPolicyException(Throwable arg0) {
		super(arg0);
	}

}
